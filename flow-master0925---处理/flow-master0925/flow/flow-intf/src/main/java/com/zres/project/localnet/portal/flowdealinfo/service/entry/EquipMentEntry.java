package com.zres.project.localnet.portal.flowdealinfo.service.entry;

public class EquipMentEntry {
    private String srvOrdId;
    private String specialtyCode;
    private String isRecycle;
    private String recycleCount;
    private String equipSequence;
    private String removeEquip;
    private String createDate;

    public String getSrvOrdId() {
        return srvOrdId;
    }

    public void setSrvOrdId(String srvOrdId) {
        this.srvOrdId = srvOrdId;
    }

    public String getSpecialtyCode() {
        return specialtyCode;
    }

    public void setSpecialtyCode(String specialtyCode) {
        this.specialtyCode = specialtyCode;
    }

    public String getIsRecycle() {
        return isRecycle;
    }

    public void setIsRecycle(String isRecycle) {
        this.isRecycle = isRecycle;
    }

    public String getRecycleCount() {
        return recycleCount;
    }

    public void setRecycleCount(String recycleCount) {
        this.recycleCount = recycleCount;
    }

    public String getEquipSequence() {
        return equipSequence;
    }

    public void setEquipSequence(String equipSequence) {
        this.equipSequence = equipSequence;
    }

    public String getRemoveEquip() {
        return removeEquip;
    }

    public void setRemoveEquip(String removeEquip) {
        this.removeEquip = removeEquip;
    }

    public String getCreateDate() {
        return createDate;
    }

    public void setCreateDate(String createDate) {
        this.createDate = createDate;
    }
}
